$(".drpBtn").click(function() {
    $(".menu").slideToggle();
    $(".menu").css("opacity", "0.5");
});

$(".menu").mouseover(function() {
    $(this).css("opacity", "1");
});

$(".menu").mouseout(function() {
    $(this).css("opacity", "0.5");
});

